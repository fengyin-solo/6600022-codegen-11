const BOARD_SIZE = 15;
const E = 0;
const B = 1;
const W = 2;

function emptyBoard(): number[][] {
  return Array.from({ length: BOARD_SIZE }, () => Array(BOARD_SIZE).fill(E));
}

function setStones(board: number[][], stones: [number, number, number][]): number[][] {
  const b = board.map(row => [...row]);
  for (const [r, c, p] of stones) {
    b[r][c] = p;
  }
  return b;
}

